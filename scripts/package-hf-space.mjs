#!/usr/bin/env node
import { cp, mkdir, rm, stat, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const repoRoot = path.resolve(path.dirname(__filename), "..");
const outputArg = process.argv[2] ?? "dist/huggingface-space";
const outputDir = path.resolve(repoRoot, outputArg);

function assertInsideRepo(targetPath) {
  const relative = path.relative(repoRoot, targetPath);
  if (relative.startsWith("..") || path.isAbsolute(relative)) {
    throw new Error(`Refusing output outside repository: ${targetPath}`);
  }
}

async function mustExist(relativePath) {
  const absolutePath = path.join(repoRoot, relativePath);
  await stat(absolutePath);
  return absolutePath;
}

async function copyFileOrDir(fromRelative, toRelative) {
  const from = await mustExist(fromRelative);
  const to = path.join(outputDir, toRelative);
  await cp(from, to, {
    recursive: true,
    force: true,
    errorOnExist: false,
    filter: (source) => {
      const name = path.basename(source);
      return name !== "__pycache__" && !name.endsWith(".pyc") && !name.endsWith(".pyo");
    },
  });
}

async function main() {
  assertInsideRepo(outputDir);
  await rm(outputDir, { recursive: true, force: true });
  await mkdir(outputDir, { recursive: true });

  await copyFileOrDir("deploy/huggingface/README.md", "README.md");
  await copyFileOrDir("services/ai/Dockerfile", "Dockerfile");
  await copyFileOrDir("services/ai/.dockerignore", ".dockerignore");
  await copyFileOrDir("services/ai/requirements.txt", "requirements.txt");
  await copyFileOrDir("services/ai/app", "app");
  await copyFileOrDir("services/ai/README.md", "SERVICE_README.md");
  await copyFileOrDir("services/ai/OCR.md", "OCR.md");

  await writeFile(
    path.join(outputDir, "GENERATED_SOURCE.txt"),
    [
      "Generated by scripts/package-hf-space.mjs.",
      "Source: services/ai plus deploy/huggingface/README.md.",
      "Do not edit generated output directly.",
      "",
    ].join("\n"),
  );

  console.log(`Packaged Hugging Face Space source at ${path.relative(repoRoot, outputDir)}`);
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error));
  process.exitCode = 1;
});
